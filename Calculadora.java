package AulaAldo;

public class Calculadora {
    public int operando1;
    public int operando2;

    int adicao() {
       return operando1 + operando2;
    }

    int subtracao() {
       return operando1 - operando2;
    }

    int multiplicacao() {
       return operando1 * operando2;
    }

    int divisao() {
       return operando1 / operando2;
    }
}